import Foundation

enum DevideCharacter: Character {
    case squareBracketOpen = "["
    case squareBracketClose = "]"
    case curlyBracketOpen = "{"
    case curlyBracketClose = "}"
    case colon = ":"
    case whiteSpace = " "
}
